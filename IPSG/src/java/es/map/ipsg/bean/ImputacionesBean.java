package es.map.ipsg.bean;



/**
 * El Class IntermediacionDesempleoBean.
 */
public class ImputacionesBean {
	
	/** el literal. */
	private String literal;
	
	/** los enteros. */
	private Long enteros;
	
	/** los decimales.  */
	private Long decimales;

	/**
	 * @return the literal
	 */
	public String getLiteral() {
		return literal;
	}

	/**
	 * @param literal the literal to set
	 */
	public void setLiteral(String literal) {
		this.literal = literal;
	}

	/**
	 * @return the enteros
	 */
	public Long getEnteros() {
		return enteros;
	}

	/**
	 * @param enteros the enteros to set
	 */
	public void setEnteros(Long enteros) {
		this.enteros = enteros;
	}

	/**
	 * @return the decimales
	 */
	public Long getDecimales() {
		return decimales;
	}

	/**
	 * @param decimales the decimales to set
	 */
	public void setDecimales(Long decimales) {
		this.decimales = decimales;
	}
	

}
