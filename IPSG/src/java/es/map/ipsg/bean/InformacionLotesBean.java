package es.map.ipsg.bean;


/**
 * El Class InformacionLotesBean.
 */
public class InformacionLotesBean {
	
	/** el id peticion. */
	private String idPeticion;
	
	/** el descripcion consulta. */
	private String descripcionConsulta;
	
	
	/**
	 * Obtiene el id peticion.
	 *
	 * @return el id peticion
	 */
	public String getIdPeticion() {
		return idPeticion;
	}
	
	/**
	 * Establece el id peticion.
	 *
	 * @param idPeticion el nuevo id peticion
	 */
	public void setIdPeticion(String idPeticion) {
		this.idPeticion = idPeticion;
	}
	
	/**
	 * Obtiene el descripcion consulta.
	 *
	 * @return el descripcion consulta
	 */
	public String getDescripcionConsulta() {
		return descripcionConsulta;
	}
	
	/**
	 * Establece el descripcion consulta.
	 *
	 * @param descripcionConsulta el nuevo descripcion consulta
	 */
	public void setDescripcionConsulta(String descripcionConsulta) {
		this.descripcionConsulta = descripcionConsulta;
	}

	
	
	
}
