package es.map.ipsc.bean;


/**
 * El Class AvisoBean.
 */
public class AvisoBean {
	
	/** el id. */
	private Integer id;
	
	/** el texto. */
	private String texto;
	
	/** el fecha inicio. */
	private String fechaInicio;
	
	/** el fecha fin. */
	private String fechaFin;
	
	/** el id estado aviso. */
	private String idEstadoAviso;
	
	
	/**
	 * Obtiene el id.
	 *
	 * @return el id
	 */
	public Integer getId() {
		return id;
	}
	
	/**
	 * Establece el id.
	 *
	 * @param id el nuevo id
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	
	/**
	 * Obtiene el texto.
	 *
	 * @return el texto
	 */
	public String getTexto() {
		return texto;
	}
	
	/**
	 * Establece el texto.
	 *
	 * @param texto el nuevo texto
	 */
	public void setTexto(String texto) {
		this.texto = texto;
	}
	
	/**
	 * Obtiene el fecha inicio.
	 *
	 * @return el fecha inicio
	 */
	public String getFechaInicio() {
		return fechaInicio;
	}
	
	/**
	 * Establece el fecha inicio.
	 *
	 * @param fechaInicio el nuevo fecha inicio
	 */
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	
	/**
	 * Obtiene el fecha fin.
	 *
	 * @return el fecha fin
	 */
	public String getFechaFin() {
		return fechaFin;
	}
	
	/**
	 * Establece el fecha fin.
	 *
	 * @param fechaFin el nuevo fecha fin
	 */
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	
	/**
	 * Obtiene el id estado aviso.
	 *
	 * @return el id estado aviso
	 */
	public String getIdEstadoAviso() {
		return idEstadoAviso;
	}
	
	/**
	 * Establece el id estado aviso.
	 *
	 * @param idEstadoAviso el nuevo id estado aviso
	 */
	public void setIdEstadoAviso(String idEstadoAviso) {
		this.idEstadoAviso = idEstadoAviso;
	}
	
	
	
	
}
