package es.map.ipsc.bean;


/**
 * El Class TipoServicioBean.
 */
public class TipoServicioBean {
	
	/** el tipo servicio. */
	private String tipoServicio;
	
	/** el descripcion. */
	private String descripcion;
	
	/** el codigo. */
	private String codigo;
	
	/**
	 * Obtiene el tipo servicio.
	 *
	 * @return el tipo servicio
	 */
	public String getTipoServicio() {
		return tipoServicio;
	}
	
	/**
	 * Establece el tipo servicio.
	 *
	 * @param tipoServicio el nuevo tipo servicio
	 */
	public void setTipoServicio(String tipoServicio) {
		this.tipoServicio = tipoServicio;
	}
	
	/**
	 * Obtiene el descripcion.
	 *
	 * @return el descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	
	/**
	 * Establece el descripcion.
	 *
	 * @param descripcion el nuevo descripcion
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	/**
	 * Obtiene el codigo.
	 *
	 * @return el codigo
	 */
	public String getCodigo() {
		return codigo;
	}
	
	/**
	 * Establece el codigo.
	 *
	 * @param codigo el nuevo codigo
	 */
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	
	
}
