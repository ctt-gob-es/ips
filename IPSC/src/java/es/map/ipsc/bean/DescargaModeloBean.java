package es.map.ipsc.bean;


/**
 * El Class DescargaModeloBean.
 */
public class DescargaModeloBean {
	
	/** el id descarga modelo. */
	private String idDescargaModelo;
	
	/** el fecha. */
	private String fecha;
	
	/** el id convocatoria. */
	private String idConvocatoria;
	
	/**
	 * Obtiene el id descarga modelo.
	 *
	 * @return el id descarga modelo
	 */
	public String getIdDescargaModelo() {
		return idDescargaModelo;
	}
	
	/**
	 * Establece el id descarga modelo.
	 *
	 * @param idDescargaModelo el nuevo id descarga modelo
	 */
	public void setIdDescargaModelo(String idDescargaModelo) {
		this.idDescargaModelo = idDescargaModelo;
	}
	
	/**
	 * Obtiene el fecha.
	 *
	 * @return el fecha
	 */
	public String getFecha() {
		return fecha;
	}
	
	/**
	 * Establece el fecha.
	 *
	 * @param fecha el nuevo fecha
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	/**
	 * Obtiene el id convocatoria.
	 *
	 * @return el id convocatoria
	 */
	public String getIdConvocatoria() {
		return idConvocatoria;
	}
	
	/**
	 * Establece el id convocatoria.
	 *
	 * @param idConvocatoria el nuevo id convocatoria
	 */
	public void setIdConvocatoria(String idConvocatoria) {
		this.idConvocatoria = idConvocatoria;
	}
	
	
}
