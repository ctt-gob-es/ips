package es.map.ipsc.bean;


/**
 * El Class ParametrosConfiguracionBean.
 */
public class ParametrosConfiguracionBean {
	
	/** el id. */
	private Integer id;
	
	/** el nombre campo. */
	private String nombreCampo;
	
	/** el valor campo. */
	private String valorCampo;
	
	/** el descripcion. */
	private String descripcion;
	
	/** el visible. */
	private Boolean visible;
	
	/**
	 * Obtiene el id.
	 *
	 * @return el id
	 */
	public Integer getId() {
		return id;
	}
	
	/**
	 * Establece el id.
	 *
	 * @param id el nuevo id
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	
	/**
	 * Obtiene el nombre campo.
	 *
	 * @return el nombre campo
	 */
	public String getNombreCampo() {
		return nombreCampo;
	}
	
	/**
	 * Establece el nombre campo.
	 *
	 * @param nombreCampo el nuevo nombre campo
	 */
	public void setNombreCampo(String nombreCampo) {
		this.nombreCampo = nombreCampo;
	}
	
	/**
	 * Obtiene el valor campo.
	 *
	 * @return el valor campo
	 */
	public String getValorCampo() {
		return valorCampo;
	}
	
	/**
	 * Establece el valor campo.
	 *
	 * @param valorCampo el nuevo valor campo
	 */
	public void setValorCampo(String valorCampo) {
		this.valorCampo = valorCampo;
	}
	
	/**
	 * Obtiene el descripcion.
	 *
	 * @return el descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	
	/**
	 * Establece el descripcion.
	 *
	 * @param descripcion el nuevo descripcion
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	/**
	 * Obtiene el visible.
	 *
	 * @return el visible
	 */
	public Boolean getVisible() {
		return visible;
	}
	
	/**
	 * Establece el visible.
	 *
	 * @param visible el nuevo visible
	 */
	public void setVisible(Boolean visible) {
		this.visible = visible;
	}


	
}
