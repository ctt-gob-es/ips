package es.map.ipsc.bean;

public class EntidadBean {
	
	private String idEntidadFinanciera;
	private String codigo;
	private String descripcion;
	private String actualizada;
	private String tipoPago;
	private String estado;
	private String apertura;
	private String cierre;
	 
	
	public String getIdEntidadFinanciera() {
		return idEntidadFinanciera;
	}
	public void setIdEntidadFinanciera(String idEntidadFinanciera) {
		this.idEntidadFinanciera = idEntidadFinanciera;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getActualizada() {
		return actualizada;
	}
	public void setActualizada(String actualizada) {
		this.actualizada = actualizada;
	}
	public String getTipoPago() {
		return tipoPago;
	}
	public void setTipoPago(String tipoPago) {
		this.tipoPago = tipoPago;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getApertura() {
		return apertura;
	}
	public void setApertura(String apertura) {
		this.apertura = apertura;
	}
	public String getCierre() {
		return cierre;
	}
	public void setCierre(String cierre) {
		this.cierre = cierre;
	}
	
}
